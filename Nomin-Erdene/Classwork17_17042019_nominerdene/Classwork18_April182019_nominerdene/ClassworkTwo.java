
public class ClassworkTwo
{
  private int a;
  private int b;
  
  public ClassworkTwo(int parameter1, int parameter2)
  {
      a = parameter1;
      b = parameter2;
  }
  public int getA()
  {
      return a;
  }
  public int getB()
  {
      return b;
  }
  public int getSum()
  {
      return a + b;
  }
  public int getQuotient()
  {
      return a * b;
  }
}
