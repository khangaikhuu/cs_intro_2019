
public class Calculator
{
    private int a;
    private int b;
   
    public int calculateAdd(int a, int b)
    {
        return a + b;
    }
    public int calculateSubtract( int a, int b)
     {
         return a - b;
        }
    public int calculateMultiply( int a, int b)
    {
        return a * b;
    }
    public int calculateDivision( int a, int b)
    {
        return a / b;
    }
    public int calculateModulo( int a, int b)
    {
        return a % b;
    }
        
     
     
  
}
