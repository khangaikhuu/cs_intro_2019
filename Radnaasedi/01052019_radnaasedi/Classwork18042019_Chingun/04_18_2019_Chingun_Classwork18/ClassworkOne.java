
public class ClassworkOne
{
   private int murda = 1; 
   
   public ClassworkOne(int a) 
   { 
       murda = a; 
    } 
   public int getCide(int murda) 
   { 
       return murda; 
    } 
   public int multiply(int par) 
   { 
       for (int i = 1; i <= par; i++) 
       { 
           murda = murda * murda; 
        } 
        return murda; 
    } 
   
}
