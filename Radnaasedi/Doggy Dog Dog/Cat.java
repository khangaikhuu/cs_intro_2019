
public class Cat
{
    public int legs = 4;
    public int age = 6;
    public boolean eatsMouse = true;
    
    public Cat()
    {
    }
    public Cat(int x, int y, boolean z)
    {
        legs = x;
        age = y;
        eatsMouse = z;
    }
    public int getlegs()
    {
        return legs;
        
    }
    public int getage()
    {
        return age;
    }
    public boolean geteatsMouse()
    {
        return eatsMouse;
    }
    
  
}
