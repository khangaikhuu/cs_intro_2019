
public class Cat
{
    public int legs = 4;
    public int age = 8;
    public boolean eatsMouse = true;
    
    public Cat (int x, int y, boolean z)
    {
        legs = x;
        age = y;
        eatsMouse = z;
    }
    public int legs()
    {
        return legs;
    }
    public int age()
    {
        return age;
    }
    public boolean eatsMouse()
    { 
        return eatsMouse;
}
}