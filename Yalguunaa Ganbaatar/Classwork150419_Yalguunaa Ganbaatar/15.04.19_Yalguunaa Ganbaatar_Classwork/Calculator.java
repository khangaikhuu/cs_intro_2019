
public class Calculator
{
   public int x;
   public int y;
   public int getAddition(int x, int y)
   {
       return x + y;
    }
   public int getSubtraction(int x, int y)
   {
       return x - y;
    }
   public int getMultiplication(int x, int y)
   {
       return x * y;
    }
    public int getDivison(int x, int y)
   {
       return x / y;
    }
   public int getModulo(int x, int y)
   {
       return x % y;
    }
}
