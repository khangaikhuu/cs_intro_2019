

public class ClassworkTwo
{
   private int kill; 
   private int slit; 
   public ClassworkTwo(int jahseh, int wrist)
   { 
       kill = jahseh;
       slit = wrist; 
    } 
   public int getExd(int kill, int slit) 
   { 
       return kill + slit; 
    } 
   public int getBruh(int kill, int slit)
   { 
       return kill * slit; 
    } 
   public int multiply(int par) 
   {  
      int hehe = 1; 
      for (int i = 1; i <= par; i++) 
      { 
          hehe = hehe * par;
        }
        return hehe;
    }
}
