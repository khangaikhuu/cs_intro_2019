
public class Calculator
{
    public int calculateAddition(int a, int b)
    {
        return a + b;
    }
    public int calculateSubtraction(int a, int b)
    {
        return a - b;
    }
    public double calculateDivision(int a, int b)
    {
        return a / b;
    }
    public int calculateMultiplication(int a, int b)
    {
        return a * b;
    }
    public int calculateModulo(int a, int b)
    {
        return a % b;
    }
}
