
public class PrintMyAllElements
{
    private String[] myArray = {"hello", "World", "Test", "Final", "Exam", "Grades", "Falling Exam", "Graduation", "Preparation"};
    private int i;
    public void PrintMyAllElements()
    {
        for (i = 0; i < myArray.length; i++)
        {
            System.out.println(myArray[i]);
        }
    }
    
}
