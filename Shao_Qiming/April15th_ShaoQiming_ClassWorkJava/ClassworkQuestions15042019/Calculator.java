
public class Calculator
{
    public int x;
    public int y;
    public double inter;
    public int getSum(int x, int y)
    {
        return x + y;
    
    }
    public int getDifference(int x, int y)
    {
        return x - y;
    
    }
    public int getProduct(int x, int y)
    {
        return x * y;
        
    }
    public int getQuotient(int x, int y)
    {
        return x / y;
    }
    public int getModulo(int x, int y)
    {
        return x % y;
    }
}
