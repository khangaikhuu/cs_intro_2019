
public class Calculator
{
   public int calculateAddition(int num1,int num2)
   {
       return num1 + num2;
   }
   public int calculateSubtraction(int num1,int num2)
   {
       return num1 - num2;
   }
   public int calculateMultiplication(int num1,int num2)
   {
       return num1 * num2;
   }
   public int calculateDivision(int num1, int num2)
   {
       return num1 / num2;
   }
   public int calculateModulo(int num1, int num2)
   {
      return num1 % num2; 
   }
   
}
