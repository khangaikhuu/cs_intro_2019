
public class Mathematik
{
    public int x;
    public int y;
    public int z;
    public int calculateAddition(int a, int b)
    {
        return a + b;
    }
    public int calculateSubstraction(int a, int b)
    {
        return a - b;
    }
    public double calculateDivision(int a, int b)
    {
        return a / b;
    }
    public int calculateMultiplication(int a, int b)
    {
        return a * b;
    }
}