

public class Cat
{
    public int legs = 4;
    public int age = 8;
    public boolean eatsMouse = true;
    public Cat()
    {
    }
    public Cat(int X, int Y, boolean Z)
    {
        legs = X;
        age = Y;
        eatsMouse = Z;
    }
    public int getLegs()
    {
        return legs;
    }
    public int getsAge()
    {
        return age;
    }
    public boolean getsEatsMouse()
    {
        return eatsMouse;
    }
}
