
public class ClassworkTwo
{
    private float x = 0.0f;
    private double y = 0.0;
    
    public ClassworkTwo(float abc, double cde)
    {
        x = abc;
        y = cde;
    }
    
    public float getX()
    {
        return x;
    }
    public double getY()
    {
        return y;
    }
    public void setX(float var)
    {
        x = var;
    }
    public void setY(double var)
    {
        y = var;
    }
}
