
public class human
{
   private int height;
   private int weight;
   private boolean isMan;
   private String educated;
   
   
   public human(int h, int w)
   {
       height = height + h;
       weight = weight + w;
       isMan = false;
       educated = "Not educated";
       
       
    }
    public int getHeight()
    {
        return height;
    }
    
}
