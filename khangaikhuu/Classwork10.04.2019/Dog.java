public class Dog
{
    public int legs = 5;
    public int age = 15;
    public boolean canBark = true;
    
    public boolean getCanBark()
    {
        return canBark;
    }
    public Dog()
    {
    }
    public Dog(int a, int b)
    {
        legs = a;
        age = b;
    }
    public int getAge()
    {
        return age;
    }
    
    
}
