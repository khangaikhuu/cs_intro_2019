
public class Cat
{
    public int legs = 4;
    public int age = 12;
    public boolean eatsMouse = false;
    
    public Cat()
    {
    }
    
    public Cat(int x, int y, boolean z)
    {
        legs = x;
        age = y;
        eatsMouse = z;
    }
    
    public int getAge()
    {
        return age;
    }
    
    public int getlegs()
    {
        return legs;
    }
    
    public boolean geteatsMouse()
    {
        return eatsMouse;
    }
    
    }
   