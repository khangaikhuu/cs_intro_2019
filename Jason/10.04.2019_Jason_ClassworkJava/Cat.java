
public class Cat
{
  public int legs = 4;
  public int age = 10;
  public boolean eatMouse = true;
  public Cat (int x, int y, boolean z)
  {
      legs = x;
      age = y;
      eatMouse = z;
  }
  public int getLegs()
  {
      return legs;
  }
  public int getAge()
  {
    return age;  
   }
   public boolean geteatMouse()
   {
       return eatMouse;
   }
}
